package Util;

public class CoordinatesMapXY {
    int map, x, y;

    public CoordinatesMapXY(int map, int x, int y) {
        this.map = map;
        this.x = x;
        this.y = y;
    }

    public int getMap() { return map; }

    public int getX() { return x; }

    public int getY() { return y; }
}
